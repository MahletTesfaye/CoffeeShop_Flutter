class CoffeeItem {
  final String name;
  final String category;
  final String image;
  final String description;
  final double price;
  final double rate;
  final int review;

  CoffeeItem({
    required this.name,
    required this.category,
    required this.image,
    required this.description,
    required this.price,
    required this.rate,
    required this.review,
  });
}
